constexpr int exp_guard = 12;

// Compute floor(exp(frac / 4^k) * 4^p) via Taylor series.
// Requires: frac <= 0 and |frac| <= 4^k (i.e., frac/4^k in [-1, 0]).
template <container C>
constexpr z<C> exp_taylor(const z<C>& frac, int k, int p) {
  const int wp = p + exp_guard;
  auto sum = mul_4exp(one<C>(), wp);  // 1 * 4^wp
  auto term = sum;

  if (is_zero(frac)) {
    return mul_4exp(one<C>(), p);
  }

  auto denom = mul_4exp(one<C>(), k);  // 4^k
  for (int i = 1;; ++i) {
    // term = term * frac / (i * 4^k)  (signed: frac may be negative)
    auto i_z = create<C>(i);
    auto divisor = mul_4exp(i_z, k);
    term = mul(term, frac);
    auto [q, _] = floor_div(term, divisor);
    term = std::move(q);

    if (is_zero(term)) {
      break;
    }
    sum = add(sum, term);
  }

  // shift from wp back to p
  return mul_4exp(sum, -(exp_guard));
}

// Compute floor(e * 4^p) via Taylor series e = sum 1/k!
template <container C>
constexpr z<C> compute_e(int p) {
  const int wp = p + exp_guard;
  auto sum = mul_4exp(one<C>(), wp);  // 1 * 4^wp
  auto term = sum;

  for (int i = 1;; ++i) {
    auto [q, _] = floor_div(term, create<C>(i));
    term = std::move(q);
    if (is_zero(term)) {
      break;
    }
    sum = add_n(sum, term);
  }

  return mul_4exp(sum, -(exp_guard));
}

// Fixed-point multiply: floor(a * b / 4^prec)
template <container C>
constexpr z<C> fp_mul(const z<C>& a, const z<C>& b, int prec) {
  auto prod = mul(a, b);
  return mul_4exp(prod, -prec);
}

// Binary exponentiation in fixed-point: compute floor(base^exp * 4^prec)
// base is a fixed-point value at precision prec (i.e., represents base / 4^prec).
template <container C>
constexpr z<C> fp_pow(const z<C>& base, int exp, int prec) {
  if (exp == 0) return mul_4exp(one<C>(), prec);
  if (exp == 1) return base;

  auto result = mul_4exp(one<C>(), prec);
  auto b = base;
  int e = exp;
  while (e > 0) {
    if (e & 1) {
      result = fp_mul<C>(result, b, prec);
    }
    e >>= 1;
    if (e > 0) {
      b = fp_mul<C>(b, b, prec);
    }
  }
  return result;
}

// Compute v such that |exp(num / 4^k) - v / 4^p| < 1 / 4^p.
template <container C>
constexpr z<C> exp_rational(z<C> num, int k, int p) {
  if (is_zero(num)) {
    return mul_4exp(one<C>(), p);
  }

  // Decompose: m = floor(num / 4^k) + 1, frac = num - m * 4^k
  // so frac / 4^k is in [-1, 0).
  auto bk = mul_4exp(one<C>(), k);  // 4^k
  auto [q_div, _] = floor_div(num, bk);
  auto m = add(q_div, one<C>());     // m = floor(num/4^k) + 1  (signed)
  auto frac = sub(num, mul(m, bk));  // frac = num - m * 4^k    (signed)

  // Determine sign of m
  bool m_negative = is_negative(m);
  int m_abs_int = 0;
  {
    auto m_abs = m;
    if (m_negative) negate(m_abs);
    // Convert small z to int for exponent (m is bounded by |x| + 1)
    if (!is_zero(m_abs)) {
      // Extract value: m_abs should fit in a reasonable int
      auto m_str = is_zero(m_abs) ? std::string("0") : std::string{};
      if (m_str.empty()) {
        // Simple extraction for small values
        auto tmp = m_abs;
        while (!is_zero(tmp)) {
          auto [qt, r] = div_n(tmp, ten<C>());
          m_str.push_back(is_zero(r) ? '0' : static_cast<char>(r.digits[0]) + '0');
          tmp = std::move(qt);
        }
        std::ranges::reverse(m_str);
      }
      for (char c : m_str) {
        m_abs_int = m_abs_int * 10 + (c - '0');
      }
    }
  }

  // Extra precision for intermediate computations
  const int extra = exp_guard + std::max(0, m_abs_int / 2) + 5;
  const int wp = p + extra;

  // Compute exp(frac / 4^k) at precision wp
  auto exp_frac = exp_taylor<C>(frac, k, wp);

  if (m_abs_int == 0) {
    // exp(num/4^k) = exp(frac/4^k)
    return mul_4exp(exp_frac, -(extra));
  }

  // Compute e at precision wp
  auto e_val = compute_e<C>(wp);

  // Compute e^|m| at precision wp using binary exponentiation
  auto e_pow = fp_pow<C>(e_val, m_abs_int, wp);

  if (m_negative) {
    // e^m = 1/e^|m|, compute as 4^(2*wp) / e_pow
    auto inv_num = mul_4exp(one<C>(), 2 * wp);
    auto [inv_q, __] = floor_div(inv_num, e_pow);
    e_pow = std::move(inv_q);
  }

  // result = exp_frac * e_pow / 4^wp (fixed-point multiply)
  auto result = fp_mul<C>(exp_frac, e_pow, wp);

  // shift from wp to p
  return mul_4exp(result, -(extra));
}

// Compute floor(log_e(1 + 1/4^n) * 4^prec).
// log(1 + z) = z - z^2/2 + z^3/3 - ...  for z = 1/4^n.
template <container C>
constexpr z<C> log_near_one_plus(int n, int prec) {
  if (prec < 0) {
    // For non-positive precision, log(1 + 1/4^n) < 1/4^n < 1
    // so floor(log(1+1/4^n) * 4^prec) = 0
    return zero<C>();
  }
  const int wp = prec + exp_guard;
  // z = 1/4^n, in fixed-point at wp: z_fp = 4^(wp-n)
  if (wp < n) return zero<C>();

  auto sum = mul_4exp(one<C>(), wp - n);  // z * 4^wp = 4^(wp-n)
  auto z_pow = sum;                       // z^1 in fixed-point
  auto z_fp = sum;                        // z in fixed-point

  for (int i = 2;; ++i) {
    // z_pow = z_pow * z / 4^wp (but z = 1/4^n, so z_pow * 1/4^n)
    z_pow = mul_4exp(z_pow, -n);
    if (is_zero(z_pow)) break;

    auto [term, _] = floor_div(z_pow, create<C>(i));
    if (is_zero(term)) break;

    if (i % 2 == 0) {
      sum = sub(sum, term);  // even terms are subtracted
    } else {
      sum = add(sum, term);  // odd terms are added
    }
  }

  return mul_4exp(sum, -(exp_guard));
}

// Compute floor(log_e(1 - 1/4^n) * 4^prec).
// log(1 - z) = -z - z^2/2 - z^3/3 - ...  for z = 1/4^n.
// Result is negative.
template <container C>
constexpr z<C> log_near_one_minus(int n, int prec) {
  if (prec < 0) {
    // log(1 - 1/4^n) is in (-1, 0) for n >= 1
    // floor(log(1-1/4^n) * 4^prec) = -1 or 0
    return zero<C>();
  }
  const int wp = prec + exp_guard;
  if (wp < n) {
    // The leading term -1/4^n * 4^wp = -4^(wp-n) which is 0 when wp < n
    auto r = create<C>(-1);
    return r;
  }

  // All terms are negative: -z - z^2/2 - z^3/3 - ...
  auto sum = zero<C>();
  auto z_pow = mul_4exp(one<C>(), wp - n);  // z^1 in fixed-point

  for (int i = 1;; ++i) {
    if (is_zero(z_pow)) break;
    auto [term, _] = floor_div(z_pow, create<C>(i));
    if (is_zero(term)) break;

    sum = add_n(sum, term);  // accumulate |terms|

    // z_pow *= z = z_pow / 4^n
    z_pow = mul_4exp(z_pow, -n);
  }

  // result is -sum
  negate(sum);
  return mul_4exp(sum, -(exp_guard));
}

// Compute d = max(-p, ceil(c * log_4(e) * 4^(-msd_x)))
// Uses upper bound: log_4(e) < 72135/100000
template <container C>
constexpr int compute_d(int c, int msd_x, int p) {
  // c * log_4(e) * 4^(-msd_x)
  // For msd_x >= 0: 4^(-msd_x) <= 1, result is small
  // For msd_x < 0: 4^(-msd_x) = 4^|msd_x|, result can be large
  //
  // We compute: ceil(c * 72135 / 100000 * 4^(-msd_x))
  // = ceil(c * 72135 * 4^(-msd_x) / 100000)

  // Use big integers to avoid overflow
  auto c_z = create<C>(c);
  auto num = mul_n(c_z, create<C>(72135));  // c * 72135
  if (-msd_x > 0) {
    mul_4exp(num, -msd_x);  // * 4^(-msd_x)
  } else if (-msd_x < 0) {
    // 4^(-msd_x) = 4^(msd_x is positive) => this means divide
    // Actually: 4^(-msd_x) where msd_x > 0 means divide by 4^msd_x
    mul_4exp(num, -msd_x);  // -msd_x < 0 here, so this divides
  }

  auto denom = create<C>(100000);
  auto [q, r] = floor_div(num, denom);
  int d_val = 0;
  if (!is_zero(r)) {
    q = add_n(q, one<C>());  // ceil
  }

  // Convert q to int
  if (!is_zero(q)) {
    auto tmp = q;
    tmp.sgn = sign::positive;
    std::string s;
    while (!is_zero(tmp)) {
      auto [qt, rm] = div_n(tmp, ten<C>());
      s.push_back(is_zero(rm) ? '0' : static_cast<char>(rm.digits[0]) + '0');
      tmp = std::move(qt);
    }
    std::ranges::reverse(s);
    for (char ch : s) d_val = d_val * 10 + (ch - '0');
  }

  return std::max(-p, d_val);
}

// Compute k = max(0, msd_x, p + 1 + ceil(d + log_4((e+1)/2)))
// Uses upper bound: log_4((e+1)/2) < 44732/100000 < 1
// So ceil(d + 0.44732) = d + 1 for non-negative integer d.
constexpr int compute_k(int msd_x, int p, int d) {
  int k_candidate = p + 1 + d + 1;  // ceil(d + 0.44732) = d + 1
  return std::max({0, msd_x, k_candidate});
}

constexpr int log_guard = 12;

// Compute floor(ln(2) * 4^prec) via arctanh(1/3): ln(2) = 2 * arctanh(1/3)
template <container C>
constexpr z<C> compute_ln2(int prec) {
  if (prec < 0) return zero<C>();
  const int wp = prec + log_guard;
  auto [z_fp, _] = floor_div(mul_4exp(one<C>(), wp), create<C>(3));
  if (is_zero(z_fp)) return zero<C>();
  auto z2 = fp_mul<C>(z_fp, z_fp, wp);
  auto sum = z_fp;
  auto z_pow = z_fp;
  for (int i = 1;; ++i) {
    z_pow = fp_mul<C>(z_pow, z2, wp);
    if (is_zero(z_pow)) break;
    auto denom = create<C>(2 * i + 1);
    auto [term, _2] = floor_div(z_pow, denom);
    if (is_zero(term)) break;
    sum = add_n(sum, term);
  }
  mul_2exp(sum, 1);
  return mul_4exp(sum, -(log_guard));
}

// Compute floor(ln(num / 4^k) * 4^p) for positive rational num/4^k.
// Uses ln(r) = 2*arctanh((r-1)/(r+1)) for r > 1 (Section 4.5.3),
// and ln(r) = -ln(1/r) for r < 1.
// For large r, decomposes r = 4^m * r' with r' in [1, 4).
template <container C>
constexpr z<C> log_rational(z<C> num, int k, int p) {
  if (is_zero(num) || is_negative(num)) {
    throw non_positive_log_error{};
  }

  auto bk = mul_4exp(one<C>(), k);
  int rel = cmp_n(num, bk);
  if (rel == 0) return zero<C>();

  if (rel < 0) {
    // r < 1: ln(r) = -ln(1/r), represent 1/r = 4^k/num at precision inv_k
    const int inv_k = p + log_guard;
    auto inv_numer = mul_4exp(one<C>(), inv_k + k);
    auto [inv_num, _] = floor_div(inv_numer, num);
    auto result = log_rational<C>(std::move(inv_num), inv_k, p);
    negate(result);
    return result;
  }

  // r = num / 4^k > 1
  // Decompose r = 4^m * r' where r' in [1, 4) for faster arctanh convergence
  int m = 0;
  {
    int bl = bit_length(num.digits);
    m = std::max(0, (bl - 1) / 2 - k);
    // Adjust m upward if num >= 4^(k+m+1)
    auto upper = mul_4exp(one<C>(), k + m + 1);
    while (cmp_n(num, upper) >= 0) {
      ++m;
      upper = mul_4exp(one<C>(), k + m + 1);
    }
    // Adjust m downward if num < 4^(k+m)
    if (m > 0) {
      auto lower = mul_4exp(one<C>(), k + m);
      while (cmp_n(num, lower) < 0 && m > 0) {
        --m;
        lower = mul_4exp(one<C>(), k + m);
      }
    }
  }

  const int wp = p + log_guard;

  // Compute ln(r') = 2 * arctanh(y), y = (num - 4^(k+m)) / (num + 4^(k+m))
  auto bk_m = mul_4exp(one<C>(), k + m);
  auto nm = sub(num, bk_m);
  auto np = add_n(num, bk_m);

  z<C> ln_rprime;
  if (is_zero(nm)) {
    ln_rprime = zero<C>();
  } else {
    auto y_num = mul_4exp(nm, wp);
    auto [y_fp, _2] = floor_div(y_num, np);
    auto y2 = fp_mul<C>(y_fp, y_fp, wp);
    auto sum = y_fp;
    auto y_pow = y_fp;
    for (int i = 1;; ++i) {
      y_pow = fp_mul<C>(y_pow, y2, wp);
      if (is_zero(y_pow)) break;
      auto [term, _3] = floor_div(y_pow, create<C>(2 * i + 1));
      if (is_zero(term)) break;
      sum = add_n(sum, term);
    }
    ln_rprime = sum;
    mul_2exp(ln_rprime, 1);
  }

  if (m == 0) {
    return mul_4exp(ln_rprime, -(log_guard));
  }

  // ln(r) = 2m * ln(2) + ln(r')
  auto ln2_val = compute_ln2<C>(wp);
  auto m_contrib = mul(create<C>(2 * m), ln2_val);
  auto result = add(m_contrib, ln_rprime);
  return mul_4exp(result, -(log_guard));
}

constexpr int atan_guard = 12;

// Compute floor(arctan(1/n) * 4^prec) via alternating Taylor series:
// arctan(1/n) = 1/n - 1/(3n^3) + 1/(5n^5) - ...
// Convergence ratio 1/n^2; fast for n >= 2.
template <container C>
constexpr z<C> atan_reciprocal(int n, int prec) {
  if (prec < 0) return zero<C>();
  const int wp = prec + atan_guard;
  auto n2 = create<C>(static_cast<long long>(n) * n);
  auto [power, _] = floor_div(mul_4exp(one<C>(), wp), create<C>(n));
  if (is_zero(power)) return zero<C>();
  auto sum = power;
  for (int i = 1;; ++i) {
    auto [q, _2] = floor_div(power, n2);
    power = std::move(q);
    if (is_zero(power)) break;
    auto [term, _3] = floor_div(power, create<C>(2 * i + 1));
    if (is_zero(term)) break;
    if (i % 2 == 1)
      sum = sub(sum, term);
    else
      sum = add_n(sum, term);
  }
  return mul_4exp(sum, -(atan_guard));
}

// Compute floor(pi * 4^prec) via Gauss's formula:
// pi/4 = 12*arctan(1/18) + 8*arctan(1/57) - 5*arctan(1/239)
template <container C>
constexpr z<C> compute_pi(int prec) {
  if (prec < 0) return zero<C>();
  const int wp = prec + atan_guard;
  auto a = atan_reciprocal<C>(18, wp);
  auto b = atan_reciprocal<C>(57, wp);
  auto c = atan_reciprocal<C>(239, wp);
  auto sum = sub(add(mul(create<C>(12), a), mul(create<C>(8), b)), mul(create<C>(5), c));
  mul_4exp(sum, 1);  // multiply by 4: pi = 4 * (pi/4)
  return mul_4exp(sum, -(atan_guard));
}

// Compute arctan(a/b) * 4^prec for a >= 0, b > 0 using direct Taylor series.
// Requires 0 < a/b <= 1/2 for fast convergence (ratio y^2 <= 1/4).
template <container C>
constexpr z<C> atan_series(const z<C>& a, const z<C>& b, int prec) {
  auto [y_fp, _] = floor_div(mul_4exp(a, prec), b);
  if (is_zero(y_fp)) return zero<C>();
  auto y2 = fp_mul<C>(y_fp, y_fp, prec);
  auto sum = y_fp;
  auto y_pow = y_fp;
  for (int i = 1;; ++i) {
    y_pow = fp_mul<C>(y_pow, y2, prec);
    if (is_zero(y_pow)) break;
    auto [term, _2] = floor_div(y_pow, create<C>(2 * i + 1));
    if (is_zero(term)) break;
    if (i % 2 == 1)
      sum = sub(sum, term);
    else
      sum = add_n(sum, term);
  }
  return sum;
}

// Compute approximately arctan(num/4^k) * 4^p (within +/- 1).
// Uses argument reduction for fast convergence:
//   |y| > 1:   arctan(y) = pi/2 - arctan(1/y)
//   |y| > 1/2: arctan(y) = pi/4 + arctan((y-1)/(y+1))
//   |y| <= 1/2: direct Taylor series
template <container C>
constexpr z<C> atan_rational(z<C> num, int k, int p) {
  if (is_zero(num)) return zero<C>();

  const int wp = p + atan_guard;
  if (wp < 0) return zero<C>();

  const bool neg = is_negative(num);
  if (neg) negate(num);

  auto denom = mul_4exp(one<C>(), k);
  z<C> result;
  int rel = cmp_n(num, denom);

  if (rel > 0) {
    // |y| > 1: arctan(|y|) = pi/2 - arctan(denom/num)
    auto two_denom = mul_4exp(denom, 0);
    mul_2exp(two_denom, 1);
    if (cmp_n(two_denom, num) > 0) {
      // denom/num > 1/2: arctan(|y|) = pi/4 + arctan((num-denom)/(num+denom))
      auto [pq, _] = floor_div(compute_pi<C>(wp), create<C>(4));
      result = add(pq, atan_series<C>(sub(num, denom), add_n(num, denom), wp));
    } else {
      // denom/num <= 1/2: arctan(|y|) = pi/2 - arctan_series(denom, num)
      auto [ph, _] = floor_div(compute_pi<C>(wp), create<C>(2));
      result = sub(ph, atan_series<C>(denom, num, wp));
    }
  } else if (rel == 0) {
    // |y| = 1: arctan(1) = pi/4
    auto [pq, _] = floor_div(compute_pi<C>(wp), create<C>(4));
    result = pq;
  } else {
    // |y| < 1
    auto two_num = mul_4exp(num, 0);
    mul_2exp(two_num, 1);
    if (cmp_n(two_num, denom) > 0) {
      // |y| > 1/2: arctan(|y|) = pi/4 - arctan((denom-num)/(denom+num))
      auto [pq, _] = floor_div(compute_pi<C>(wp), create<C>(4));
      result = sub(pq, atan_series<C>(sub(denom, num), add_n(denom, num), wp));
    } else {
      // |y| <= 1/2: direct series
      result = atan_series<C>(num, denom, wp);
    }
  }

  if (neg) negate(result);
  return mul_4exp(result, -(atan_guard));
}


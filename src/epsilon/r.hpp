// Generated by AI
template <container C>
constexpr r<C> exp(r<C> x) {
  return r<C>{[x = std::move(x)](int n) -> coro::lazy<z<C>> {
    constexpr int w = 1;             // B = 4 >= 4
    const int ell = n + w;           // l = n + 1
    const int p = std::max(0, ell);  // p = max(0, l)

    // --- Test 1: very negative x -> exp(x) ~ 0 ---
    // m = 0 for B = 4 (since ceil(log_4(e/3)) = 0)
    auto x0 = co_await x.approx(0);
    auto exp_x0_p = details::exp_rational<C>(x0, 0, p);
    if (is_zero(exp_x0_p) || is_negative(exp_x0_p)) {
      co_return z<C>{};  // zero
    }

    // --- Test 2: x near zero -> exp(x) ~ 1 ---
    // Use signed comparison via sub (cmp_n is unsigned magnitude only)
    if (n > 0) {
      auto x_ell = co_await x.approx(ell);
      auto log_lo = details::log_near_one_minus<C>(n, ell);
      auto log_hi = details::log_near_one_plus<C>(n, ell);
      auto lo_bound = add(log_lo, create<C>(2));
      auto hi_bound = sub(log_hi, create<C>(2));
      auto diff_lo = sub(x_ell, lo_bound);
      auto diff_hi = sub(x_ell, hi_bound);
      bool x_gt_lo = !is_zero(diff_lo) && !is_negative(diff_lo);
      bool x_lt_hi = is_negative(diff_hi);
      if (x_gt_lo && x_lt_hi) {
        co_return mul_4exp(details::one<C>(), n);
      }
    } else {
      // For n <= 0, compute bound = floor(log_e(1 + 4^(-n))) - 1 directly.
      // Avoids Taylor series for log which loops when n = 0.
      auto x0_val = co_await x.approx(0);
      int neg_n = -n;  // >= 0
      int log_floor;
      if (neg_n == 0) {
        log_floor = 0;  // floor(ln(2)) = 0
      } else {
        // log(1 + 4^k) > k*ln(4) > k * 1.386294 (lower bound)
        log_floor = static_cast<int>(static_cast<long long>(neg_n) * 1386294LL / 1000000LL);
      }
      int bound = log_floor - 1;
      auto bound_z = create<C>(bound);
      auto diff = sub(x0_val, bound_z);
      bool x_le_bound = is_zero(diff) || is_negative(diff);
      if (x_le_bound) {
        co_return mul_4exp(details::one<C>(), n);
      }
    }

    // --- General case ---
    // Use bounded msd: if x is very close to 0 (or exactly 0),
    // msd hits the limit and we return 4^n since exp(x) ≈ 1.
    int msd_limit = std::max(p + 10, 100);
    int msd_x = co_await msd(x, msd_limit);
    if (msd_x >= msd_limit) {
      co_return mul_4exp(details::one<C>(), n);
    }
    auto x_msd = co_await x.approx(msd_x);
    auto x_msd_abs = x_msd;
    x_msd_abs.sgn = sign::positive;
    int c = (cmp_n(x_msd_abs, details::one<C>()) >= 0) ? 8 : 1;  // 2B = 8
    int d = details::compute_d<C>(c, msd_x, p);
    int k = details::compute_k(msd_x, p, d);

    auto x_k = co_await x.approx(k);
    auto v = details::exp_rational<C>(x_k, k, p);
    co_return mul_4exp(v, n - p);
  }};
}

// Generated by AI — Natural logarithm (Algorithm 8 of Ménissier-Morain's paper)
template <container C>
constexpr r<C> log(r<C> x) {
  return r<C>{[x = std::move(x)](int n) -> coro::lazy<z<C>> {
    constexpr int c = 2;         // B = 4 >= 3
    int w = c - std::min(0, n);  // w = c - min(0, n)

    int msd_x = co_await msd(x);
    int k = n + msd_x + w;

    auto xk = co_await x.approx(k);

    // v = floor(ln(xk / 4^k) * 4^(n+w))
    auto v = details::log_rational<C>(xk, k, n + w);

    // Result = floor((v + 1) / 4^w + 4^n / xk)
    //        = floor(((v + 1) * xk + 4^(n+w)) / (xk * 4^w))
    auto v_plus_1 = add(v, details::one<C>());
    auto numerator = add(mul(v_plus_1, xk), mul_4exp(details::one<C>(), n + w));
    auto denominator = xk;
    mul_4exp(denominator, w);
    auto [result, _] = floor_div(std::move(numerator), std::move(denominator));

    co_return result;
  }};
}

// Generated by AI — Arctangent (Algorithm 9 of Ménissier-Morain's paper)
template <container C>
constexpr r<C> arctan(r<C> x) {
  return r<C>{[x = std::move(x)](int n) -> coro::lazy<z<C>> {
    constexpr int w = 1;  // B = 4 >= 4
    int k = std::max(0, n + w);

    auto xk = co_await x.approx(k);

    if (is_zero(xk)) {
      co_return z<C>{};  // zero
    }

    // v = approximately arctan(xk / 4^k) * 4^(n+w) (within +/- 1)
    auto v = details::atan_rational<C>(xk, k, n + w);

    // Result = floor((v + 1) / 4^w + 4^(n+k) / (4^(2n+2) + xk^2 + xk))
    // Use common-denominator integer arithmetic.
    // shift = max(0, -(2n+2)) to keep all exponents non-negative.
    int e = 2 * n + 2;
    int shift = std::max(0, -e);
    int e_pos = std::max(0, e);  // max(0, 2n+2)

    auto xk_sq = mul(xk, xk);
    auto xk_sq_plus_xk = add(xk_sq, xk);
    auto d_int = add(mul_4exp(details::one<C>(), e_pos), mul_4exp(xk_sq_plus_xk, shift));

    auto v_plus_1 = add(v, details::one<C>());
    auto numerator = add(mul(v_plus_1, d_int), mul_4exp(details::one<C>(), w + n + k + shift));
    auto denominator = d_int;
    mul_4exp(denominator, w);
    auto [result, _] = floor_div(std::move(numerator), std::move(denominator));

    co_return result;
  }};
}

